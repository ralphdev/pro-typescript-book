// * ================ Arithmetic Operators:

let a = 10;
let b = 5;

// Addition
let sum = a + b; // sum = 15

// Subtraction
let difference = a - b; // difference = 5

// Multiplication
let product = a * b; // product = 50

// Division
let quotient = a / b; // quotient = 2

// Modulus (Remainder after division)
let remainder = a % b; // remainder = 0

// * ================ Comparison Operators:

let x = 10;
let y = 5;

// Equal to
let isEqual = x === y; // isEqual = false

// Not equal to
let isNotEqual = x !== y; // isNotEqual = true

// Greater than
let isGreaterThan = x > y; // isGreaterThan = true

// Less than
let isLessThan = x < y; // isLessThan = false

// Greater than or equal to
let isGreaterOrEqual = x >= y; // isGreaterOrEqual = true

// Less than or equal to
let isLessOrEqual = x <= y; // isLessOrEqual = false

// * ================ Logical Operators:
let p = true;
let q = false;

// Logical AND
let andResult = p && q; // andResult = false

// Logical OR
let orResult = p || q; // orResult = true

// Logical NOT
let notResult = !p; // notResult = false

// * ================ Bitwise Operators:

let num1 = 5; // Binary: 0101
let num2 = 3; // Binary: 0011

// Bitwise AND
let andResult2 = num1 & num2; // Result: 0001 (1)

// Bitwise OR
let orResult2 = num1 | num2; // Result: 0111 (7)

// Bitwise XOR (Exclusive OR)
let xorResult = num1 ^ num2; // Result: 0110 (6)

// Bitwise NOT (One's complement)
let notNum1 = ~num1; // Result: 1010 (-6)

// Bitwise Left Shift
let leftShiftResult = num1 << 1; // Result: 1010 (10)

// Bitwise Right Shift
let rightShiftResult = num1 >> 1; // Result: 0010 (2)

